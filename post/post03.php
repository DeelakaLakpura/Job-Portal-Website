            <p><strong>Pros and Cons of Negotiating a Salary Offer
            </strong></p>
            <p><u>Pro: Negotiating a Salary Offer Is a Must</u></p>
            <p class="note note-light">
                When a company finds the right candidate, it is more than likely willing to negotiate salary. Recruiters usually have a second and third choice lined up but it's never as good as the first choice. There's usually more budget available for Candidate No. 1. Obviously, companies have to abide by their budgets, but negotiating a salary offer is just part of the hiring process. If you're at the stage where the employer is negotiating salary with you, the employer wants you—they're aware of your value. Just because you push back at their first offer doesn't mean your value is going to up and disappear. Another salary negotiation tip to know is that for some positions, you are simply expected to negotiate regardless of the offer. For example, if you're pursuing a business development or sales position, a casual acceptance of the initial offer might actually hurt your chances because it shows a neglect for a basic element of all business dealings: negotiation. Meaning, salary negotiation is a prime chance for you to show your skill and appreciation for the exercise. If you let that opportunity pass you by, the employer might think you're inept.
            </p>
            <p><u>Con: There Are Times to Avoid Salary Negotiation</u></p>
            <p>Some career experts say there are definite instances when you shouldn't be negotiating a salary offer at all. For instance:


                There's no good reason you should be paid more than you're offered
                Granted, if pushing back can reveal some of your previous accomplishments, contributions to your department, and/or prized skills that human resources overlooked in their initial review, your counteroffer might be honored. But in other cases, if you can't justify your salary ask, you risk having the offer rescinded.
                
                You're asking for too much
                Negotiating salary when there's a big gap between what the company is offering and what you want is also a waste of time. Let's say a recent grad is looking at entry-level jobs in his field of training and learns early on that the jobs are paying an admittedly low salary for the industry. This person shouldn't go through several rounds of interviews and then demand double what the employer was willing to pay. He created no room for negotiation and dug his heels in. There's no way he'll be offered the job. Negotiating a job offer is always a risk, but by not accepting the offer in a timely manner, you're leaving the door wide open for other candidates to walk right into the picture.</p>
                <p><u>Salary: One Part of a Bigger Picture</u></p>
                <p>Salary is just one component of the offer package, but it's the one that most often generates a gut response if it's too low or euphoria if it's more than you were expecting. However, what may be just as important is whether you feel passionate about what the company does, you trust your peers, and you believe the role is one in which you can truly thrive, be successful, and be happy. An average or even below-average compensation package that offers great leadership, peer support, and a product or service you believe in will likely result in you performing to the best of your abilities. In turn, you can expect bonuses, greater responsibilities, more job security, and promotions. In the end, job conditions dictate what you can achieve and, therefore, your future compensation, not the other way around.</p>
